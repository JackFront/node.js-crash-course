const os = require('os');

// Plataform (Sistema operacional)
console.log(os.platform());

// CPU Arch (Bits)
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Home directory
console.log(os.homedir());